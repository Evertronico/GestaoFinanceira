    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        //valida a remoção 
        if (this.linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(null, "Nenhum Objetivo selecionado");
            return;
        }

        //valida a ação da exclusão
        if (JOptionPane.showConfirmDialog(null, "Realmente deseja remover?",
                "Confirmação", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            //cria um objeto da classe model
            ClienteModel model = new ClienteModel();  
            //remove o objetivo e recupera a mensagem do banco de dados
            String msg = model.remova(
                    this.clientes.get(this.linhaSelecionada).getClinteId()
            );
            limparFormulario();
            atualizarTabela();
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void tblClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClienteMouseClicked
        //recupera a linha selecionada pelo usuário
        this.linhaSelecionada = tblCliente.getSelectedRow();
        
        //cria um objeto da linha selecionada.
        Cliente c = this.clientes.get(this.linhaSelecionada);
        
        //preenche o formulário com as informações do objeto selecionado.
        txtNome.setText(c.getNome() );
        txtCpf.setText(c.getCpf() );
        
        //formata a data para DD/MM/YYYY
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        txtDataNascimento.setText(sdf.format(c.getDataNascimento()) );
        
        txtEmail.setText(c.getEmail() );
        txtSenha.setText(c.getSenha() );
        
        //habilita o botão excluir.
        controlarBotoes();
    }//GEN-LAST:event_tblClienteMouseClicked

    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        try {
            // recupera a data no padrão DD/MM/AAAA como String
            String dataStr = txtDataNascimento.getText();
            java.sql.Date dataNascimento = null;

            // recupera todos os valores do formulário
            String nome = txtNome.getText();
            try {
                SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
                java.util.Date parsed = sdf.parse(dataStr);
                dataNascimento = new java.sql.Date(parsed.getTime());
            } catch (ParseException ex) {
                JOptionPane.showMessageDialog(null, "Data inválida: " + ex.getMessage());
                //sai do metodo se a data for invalida.
                return;
            }
            //
            String cpf = txtCpf.getText().replace(".","").replace("-", "") ;
            
        
            String email = txtEmail.getText();
            String senha = txtSenha.getText();
            

            // objeto da classe Objetivo
            Cliente c = new Cliente(
                this.linhaSelecionada == -1 
                    ? -1 
                    : this. clientes.get(this.linhaSelecionada).getClinteId(),
            nome,
            cpf,
            dataNascimento,
            email,
            senha
            );

            ClienteModel model = new ClienteModel();
            String msg = model.grave(c);
            JOptionPane.showMessageDialog(null, msg);

            atualizarTabela();
            limparFormulario();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnGravarActionPerformed

