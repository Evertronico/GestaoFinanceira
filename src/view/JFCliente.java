    private void atualizarTabela() {
        // cria objeto da classe model e lista a coleção de objetos
        ClienteModel model = new ClienteModel();
        clientes = model.liste();
        // carrega a tabela (JTable)
        carregarTabela(clientes);
    }

    private void controlarBotoes() {
        //método para ativar os botões.
        btnRemover.setEnabled(linhaSelecionada!= -1);
    }

    private void carregarTabela(ArrayList<Cliente> liste) {
        // captura a estrutura da tabela (JTable)
        DefaultTableModel table = (DefaultTableModel) tblCliente.getModel();
        // limpa todas as linhas da tabela
        table.setRowCount(0);
        // percorre a coleção de objetos incluindo linha por linha na tabela
        for (Cliente C : liste) {
            // adiciona uma nova linha na tabela
            table.addRow(new Object[]{
                C.getNome(), C.getCpf(), C.getDataNascimento(),C.getEmail()
            });
        }        
    }

    private void limparFormulario() {
        linhaSelecionada = -1;
        txtNome.setText("");
        txtCpf.setText("");
        txtDataNascimento.setText("");
        txtEmail.setText("");
        txtSenha.setText("");
    }


